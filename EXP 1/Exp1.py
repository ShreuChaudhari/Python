num1= int(input ("Enter the value of first number:"))
num2= int(input ("Enter the value of second number:"))

#Addition
sum = num1+num2
print(sum)

#subtraction
Diff= num1-num2
print(Diff)

#Multiplication
Multi= num1*num2
print(Multi)

#Division
Div= num1/num2
print(Div)

#Modulus
Mod = num1%num2
print(Mod)

#Exponential
Exp=num1**num2
print(Exp)

#Floor Division
FlDiv=num1//num2
print(FlDiv)